public class OrderedPair {
 double x, y;

 public OrderedPair() {
     x = 0;
     y = 0;

 }

 public OrderedPair(double xcord, double ycord) {
     x = xcord;
     y = ycord;

 }

 public String toString() {
     return "("+x+", " + y + ")";

 }

 public double getX() {
     return x;
 }

 public double getY() {
     return y;

 }




}
